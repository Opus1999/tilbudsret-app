export function searchCities(query: string): City[] {
  if (query.length < 2) return [];
  const q = query.toLowerCase();
  return danishCities
    .filter(
      (c) => c.postalCode.startsWith(query) || c.name.toLowerCase().startsWith(q)
    )
    .slice(0, 5);
}
